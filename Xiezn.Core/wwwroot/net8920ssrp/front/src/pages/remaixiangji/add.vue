                <img v-else class="upload-img" style="margin-right:20px;" v-bind:key="index" v-for="(item,index) in ruleForm.tupian.split(',')" :src="baseUrl+item" width="100" height="100">
            </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}'  label="相机类型" prop="xiangjileixing">
            <el-select v-model="ruleForm.xiangjileixing" placeholder="请选择相机类型" :disabled=" false  ||ro.xiangjileixing" >
              <el-option
                  v-for="(item,index) in xiangjileixingOptions"
                  :key="index"
